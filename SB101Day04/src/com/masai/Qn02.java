public class Qn02 {
	/*
	 * Life cycle of a thread in java consists of mainly 5 states :
	 * > New state
	 * > Runnable state
	 * > Running state
	 * > Blocked state
	 * > Dead state
	 * 
	 * New state is when a thread is just created using the new keyword.
	 * 
	 * Runnable is when we use the start keyword of the Thread class to run the thread.
	 * 
	 * Running is when the thread schedule allocates a slot for the thread to run or execute.
	 * 
	 * Blocked state is when we use sleep , wait or join method when the thread is in running state,
	 * when the sleep time gets over or when we notify from the thread, the thread again goes from blocked to runnable and then running when it gets the time.
	 * 
	 * Dead state is when the run method is done executing and the work of thread is done.
	 * 
	 * 
	 */

}
